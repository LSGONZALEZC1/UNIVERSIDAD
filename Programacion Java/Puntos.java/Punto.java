public class Punto {
    private double x;
    private double y;

    public Punto(double x_, double y_){
        this.x=x_;
        this.y=y_;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
